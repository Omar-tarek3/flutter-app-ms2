class Location{
  int id;
  String name;
  String description;
  String theme;
  String imageUrl;
  String locationUrl;

  Location({int id, String  name,  String description, String theme, String imageUrl, String locationUrl})
  {
    this.id=id;
    this.name=name;
    this.description=description;
    this.theme=theme;
    this.imageUrl=imageUrl;
    this.locationUrl=locationUrl;

  }

  }


